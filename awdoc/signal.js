    function buttap(N) {
      // Prototype analogue Butterworth low pass filter has poles on a unit circle.
      let p=[];
      for (let n=0; n<N; n++) {
        const angle = ((n + 0.5) / N + 0.5) * Math.PI;
        p.push([Math.cos(angle), Math.sin(angle)]);
      }
      if (N%2==1) p[(N-1)/2] = [-1, 0];

      return [[], p, 1];
    }

